        pieDataset.setValue("three", new Integer(30));
        pieDataset.setValue("four", new Integer(40));
        
        JFreeChart chart = ChartFactory.createPieChart("teste", pieDataset , true, true, true);
        
        
        PiePlot p=(PiePlot) chart.getPlot();
