        ChartPanel pa = new ChartPanel(chart, false);
        frame.setSize(450,500);
        pa.setSize(250,300);

         panel.add(pa, BorderLayout.CENTER);
         panel.validate();
         
         pa.setVisible(true);
         pa.setBackground(Color.black);
         
        
